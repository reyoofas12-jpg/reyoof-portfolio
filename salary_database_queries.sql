-- ٥ إجمالي تكاليف الأشهر الثلاثة
-- النتيجة: إجمالي تكاليف الأشهر الثلاثة 743,569 ريال
SELECT SUM(total_cost) AS اجمالي_التكاليف
FROM employees;

-- ٦ متوسط الراتب
SELECT ROUND(AVG(gross_salary), 2) AS متوسط_الراتب
FROM employees;

-- ٧ أعلى وأدنى راتب
-- النتيجة: أعلى راتب 104,999 وأدنى راتب 12,000 ريال
SELECT 
    MAX(gross_salary) AS اعلى_راتب,
    MIN(gross_salary) AS ادنى_راتب
FROM employees;

-- ٨ نسبة تكلفة كل موظف من الإجمالي
-- النتيجة: عبدالرزاق الفيفي يمثل أعلى نسبة من التكاليف
SELECT 
    name,
    total_cost,
    ROUND(total_cost * 100.0 / SUM(total_cost) OVER(), 2) AS النسبة_المئوية
FROM employees
ORDER BY النسبة_المئوية DESC;

-- ٩ الفرق بين الراتب الأساسي والمجمل
SELECT 
    name,
    basic_salary,
    gross_salary,
    ROUND(gross_salary - basic_salary, 2) AS الفرق
FROM employees
ORDER BY الفرق DESC;

-- ١٠ مقارنة إجمالي التكاليف بين الأشهر
-- النتيجة: ديسمبر أقل تكلفة بسبب غياب عبدالرزاق الفيفي
SELECT 
    month,
    SUM(total_cost) AS اجمالي_التكاليف
FROM employees
GROUP BY month
ORDER BY month;

-- ١١ مقارنة راتب كل موظف بين الأشهر
SELECT 
    name,
    month,
    gross_salary
FROM employees
ORDER BY name, month;

-- ١٢ عدد الموظفين في كل شهر
-- النتيجة: نوفمبر وأكتوبر 6 موظفين، ديسمبر 5 موظفين
SELECT 
    month,
    COUNT(name) AS عدد_الموظفين
FROM employees
GROUP BY month;

-- ١٣ إجمالي البدلات لكل موظف
-- النتيجة: عبدالرزاق الفيفي أعلى بدلات
SELECT 
    name,
    ROUND(AVG(housing_allowance), 2) AS بدل_سكن,
    ROUND(AVG(transport_allowance), 2) AS بدل_انتقال,
    ROUND(AVG(housing_allowance + transport_allowance), 2) AS اجمالي_البدلات
FROM employees
GROUP BY name
ORDER BY اجمالي_البدلات DESC;

-- ١٤ الموظفون الذين تكلفتهم أعلى من المتوسط
-- النتيجة: الموظفون الأعلى تكلفة من المتوسط العام
SELECT 
    name,
    job_title,
    total_cost
FROM employees
WHERE total_cost &gt; (SELECT AVG(total_cost) FROM employees)
GROUP BY name
ORDER BY total_cost DESC;

-- ١٥ ترتيب الموظفين حسب التكلفة داخل كل شهر
SELECT 
    name,
    month,
    total_cost,
    RANK() OVER (PARTITION BY month ORDER BY total_cost DESC) AS الترتيب
FROM employees;